function displaContact() {
    let cont = document.getElementById('infoCont');
    if (cont.style.display === 'none') {
        cont.style.display = 'block';
    } else {
        cont.style.display = 'none';
    }
}

function displaEmailForm() {
    let cont = document.getElementById('cotizarFormContainer');
    if (cont.style.display === 'none') {
        cont.style.display = 'block';
    } else {
        cont.style.display = 'none';
    }
}